package oop;

public class Circle extends Figure {

    static double calcCircleArea(double areaOfCircle, double radiusOfCircle) {
        return areaOfCircle = Math.PI * radiusOfCircle;
    }
    static double calcCircleLenght(double radiusOfCircle, double lenghtOfCircle) {
        return lenghtOfCircle = 2 * Math.PI * radiusOfCircle;
    }
}
